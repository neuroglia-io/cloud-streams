namespace CloudStreams.Dashboard;


/// <summary>
/// Enumerates all supported sort modes
/// </summary>
public enum SortMode
{
    /// <summary>
    /// Indicates no sorting
    /// </summary>
    None,
    /// <summary>
    /// Indicates an ascending sorting
    /// </summary>
    Ascending,
    /// <summary>
    /// Indicates a descending sorting
    /// </summary>
    Descending
}
